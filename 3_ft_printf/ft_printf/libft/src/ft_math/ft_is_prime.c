#include "../../../includes/libft.h"

int	ft_is_prime(int nb)
{
	int	i;

	i = ft_sqrt(nb);
	if (nb < 0)
		return (0);
	if (nb == 2)
		return (1);
	while (i >= 2)
	{
		if (nb % i == 0)
			return (0);
		i--;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	if (ft_is_prime(nb) == 1)
		return (nb);
	else
		return (ft_find_next_prime(nb + 1));
}
